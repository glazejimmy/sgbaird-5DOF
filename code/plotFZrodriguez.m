function [hFZ,hAx] = plotFZrodriguez()

% define the FZ vertices
FZvertices = [0, 0, 0;...
    sqrt(2)-1, 0, 0;...
    sqrt(2)-1, sqrt(2)-1, 0;...
    sqrt(2)-1, sqrt(2)-1, 3-2*sqrt(2);...
    1/3, 1/3, 1/3;...
    sqrt(2)-1, 1-(1/sqrt(2)), 1-(1/sqrt(2))];

% define the FZ faces
FZfaces = [1 3 2 nan;...
    2 3 4 6;...
    1 5 4 3;...
    1 2 6 5;...
    4 5 6 nan];

% plot
hFZ = patch('vertices',FZvertices,'faces',FZfaces([1:2,4:5],:),'facecolor','none','edgecolor','k');
view(75,14)
axis equal tight off vis3d
set(gcf,'color','w')
hAx = gca;

end